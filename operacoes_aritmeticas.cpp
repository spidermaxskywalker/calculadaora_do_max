#include "operacoes_aritmeticas.h"


int max::aritmetica::soma(int x, int y)
{
    return x + y;
}

int max::aritmetica::subtrai(int x, int y)
{
    return x - y;
}

int max::aritmetica::multiplica(int x, int y)
{
    return x * y;
}

int max::aritmetica::quociente(int x, int y)
{
    return x / y;
}

int max::aritmetica::resto(int x, int y)
{
    return x % y;
}
